/**
 * Secret card component.
 *
 * Displays a single secret with masked value and delete button.
 */

import type { Secret, SecretType } from '../../types';
import { SECRET_TYPE_INFO } from '../../types';

interface SecretCardProps {
  /** Secret data to display */
  secret: Secret;
  /** Callback when delete is clicked */
  onDelete: (name: string) => void;
  /** Whether delete operation is pending */
  isDeleting?: boolean;
}

/**
 * Card displaying secret metadata with delete action.
 *
 * @param props - Component props
 * @param props.secret - Secret data
 * @param props.onDelete - Delete callback
 * @param props.isDeleting - Delete in progress
 */
export function SecretCard({ secret, onDelete, isDeleting }: SecretCardProps) {
  const typeInfo = SECRET_TYPE_INFO[secret.secret_type as SecretType] || {
    label: secret.secret_type,
  };

  return (
    <div className="bg-slate-800 rounded-lg border border-slate-700 p-4 hover:border-slate-600 transition-colors">
      <div className="flex items-start justify-between">
        <div className="flex-1 min-w-0">
          <div className="flex items-center gap-2">
            <SecretTypeIcon type={secret.secret_type as SecretType} />
            <h3 className="text-lg font-semibold text-white truncate">
              {secret.name}
            </h3>
          </div>
          <p className="mt-1 text-sm text-slate-400">{typeInfo.label}</p>
        </div>

        <button
          onClick={() => onDelete(secret.name)}
          disabled={isDeleting}
          className="ml-4 p-2 text-slate-400 hover:text-red-400 hover:bg-red-500/10 rounded-md transition-colors disabled:opacity-50"
          title="Delete secret"
        >
          <TrashIcon />
        </button>
      </div>

      <div className="mt-4 flex items-center gap-2">
        <span className="px-2 py-1 text-xs font-mono bg-slate-900 text-slate-300 rounded">
          {secret.masked_value}
        </span>
      </div>

      {secret.created_at && (
        <p className="mt-3 text-xs text-slate-500">
          Created {formatDate(secret.created_at)}
        </p>
      )}
    </div>
  );
}

/** Format ISO date string */
function formatDate(isoDate: string): string {
  try {
    const date = new Date(isoDate);
    return date.toLocaleDateString('en-US', {
      month: 'short',
      day: 'numeric',
      year: 'numeric',
    });
  } catch {
    return isoDate;
  }
}

